// ── Shared drawing helpers ───────────────────────────────────────────────────
let y = 0

function draw(page, text, { f = font, size = 10.5, x = 60, color = D } = {}) {
  page.drawText(text, { x, y, size, font: f, color })
  y -= size + 7
}

function rule(page) {
  page.drawLine({
    start: { x: 60, y }, end: { x: 535, y },
    thickness: 0.5, color: rgb(0.78, 0.78, 0.78),
  })
  y -= 14
}

function gap(n = 1) { y -= 9 * n }

function section(page, title) {
  gap(0.5)
  rule(page)
  draw(page, title, { f: bold, size: 11 })
  gap(0.3)
}

